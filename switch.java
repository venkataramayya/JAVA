class Switch {
    public static void main(String[] args) {
        String s = "venkat";  // s is a String

        switch (s) {
            case "a":
                System.out.println("this is rams");
                break;
            case "b":
                System.out.println("this is rams");
                break;
            case "venkat":
                System.out.println("this is avr");
                break;
            default:
                System.out.println("no one is there");
        
    }
    }}
